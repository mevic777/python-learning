price1 = 3.131345
price2 = -923.23
price3 = 12.32

print(f"Price 1: {price1:.2f}") # for float formatting
print(f"Price 2: {price2:10}") # will offer for the string in the terminal with 10 spaces
print(f"Price 3: {price3:010}") # will put 0 in front of the string if more spaces
print(f"Price 3: {price3:<10}") # left justify with spaces
print(f"Price 3: {price3:>10}") # right justify with spaces
print(f"Price 3: {price3:^10}") # center line
print(f"Price 3: {price3:+10}") # any posivitive number will be preceeded with a + sign
print(f"Price 3: {price3:}") # will be showed evenly for all the numbers
print(f"Price 3: {price1:,}") # each thousand will be with a coma
print(f"Price 3: {price2:,}") # each thousand will be with a coma
print(f"Price 3: {price3:,}") # each thousand will be with a coma


