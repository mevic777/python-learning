--1. Să se scrie o interogare ce va intersecta toate tabelele bazei de date.
SELECT S.NUME+' '+S.PRENUME AS STUD,DATA_NASTERII,IDNP,GEN,AN_FORMARE,LIMBA,L.NUME+' '+L.PRENUME AS LECT,G.NUME AS GROP,SS.NUME AS SPECIAL,F.NUME AS FACUL
	FROM LECTOR L INNER JOIN EXAMINARE E ON L.LECTOR_ID=E.LECTOR_ID
	INNER JOIN DISCIPLINA D ON D.DISCIPLINA_ID=E.DISCIPLINA_ID
	INNER JOIN NOTA N ON N.EXAMINARE_ID=E.EXAMINARE_ID
	INNER JOIN STUDENT S ON S.STUDENT_ID=N.STUDENT_ID
	INNER JOIN GRUPA G ON G.GRUPA_ID=S.GRUPA_ID
	INNER JOIN SPECIALITATE SS ON SS.SPECIALITATE_ID=G.SPECIALITATE_ID
	INNER JOIN FACULTATE F ON F.FACULTATE_ID=SS.FACULTATE_ID

--2. Să se determine numărul de studenți pentru fiecare grupă. Formați interogarea prin două metode(simplu și select în cadrul altui select).
SELECT G.NUME,COUNT(*) AS STUDENTI
	FROM STUDENT S INNER JOIN GRUPA G ON S.GRUPA_ID=G.GRUPA_ID
	GROUP BY G.NUME


--3. Să se determine studentul ce are ce-a mai mare medie. Să se afișeze facultatea, specialitatea, grupa, nume student și nota medie.

SELECT *,
	(
		SELECT SS.NUME,AVG(NOTA)
			FROM STUDENT SS INNER JOIN NOTA N ON SS.STUDENT_ID=N.NOTA_ID
			WHERE S.STUDENT_ID=SS.STUDENT_ID
			GROUP BY SS.NUME
	)
	FROM STUDENT S

