ALTER FUNCTION X(@D_ID INT,@GEN INT,@TIP INT)
	RETURNS INT
	BEGIN
		DECLARE @S INT;
		IF @TIP=1
		SET @S=
		(
			SELECT COUNT(DISTINCT(A.ANGAJAT_ID)) 
				FROM ANGAJAT A INNER JOIN ANGAJARE AA ON AA.ANGAJAT_ID=A.ANGAJAT_ID
				WHERE AA.DEPARTAMENT_ID=@D_ID AND GEN=@GEN
		);
		ELSE IF @TIP=2
		SET @S=
		(
			SELECT IIF(SUM(SALARIU) IS NULL,0,SUM(SALARIU))
				FROM ANGAJARE AA INNER JOIN ANGAJAT A ON A.ANGAJAT_ID=AA.ANGAJAT_ID
				WHERE DEPARTAMENT_ID=@D_ID AND GEN=@GEN

		);
		RETURN @S;
	END;

CREATE FUNCTION X(@FACUL INT,@GEN INT,@TIP INT)
	RETURNS INT
	BEGIN
	DECLARE @S INT;
	IF @TIP=1
	SET @S=
	(
		SELECT COUNT(DISTINCT(S.STUDENT_ID))
			FROM STUDENT S INNER JOIN NOTA N ON N.STUDENT_ID=S.STUDENT_ID
						   INNER JOIN GRUPA G ON G.GRUPA_ID=S.GRUPA_ID
			               INNER JOIN SPECIALITATE SS ON SS.SPECIALITATE_ID=G.SPECIALITATE_ID
			               --INNER JOIN FACULTATE F ON F.FACULTATE_ID=SS.FACULTATE_ID
			               WHERE FACULTATE_ID=@FACUL AND GEN=@GEN
	);
	ELSE IF @TIP=2
	SET @S=
	(
		SELECT IIF(AVG(NOTA) IS NULL,0,AVG(NOTA))
			FROM NOTA N INNER JOIN STUDENT S ON S.STUDENT_ID=N.STUDENT_ID
			            INNER JOIN GRUPA G ON G.GRUPA_ID=S.GRUPA_ID
			            INNER JOIN SPECIALITATE SS ON SS.SPECIALITATE_ID=G.SPECIALITATE_ID
			           -- INNER JOIN FACULTATE F ON F.FACULTATE_ID=SS.FACULTATE_ID
						WHERE FACULTATE_ID=@FACUL AND GEN=@GEN
	);
	RETURN @S;
	END;

