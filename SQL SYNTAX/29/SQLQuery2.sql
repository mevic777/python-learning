ALTER FUNCTION SUMA(@A INT,@B INT,@C INT)
	RETURNS INT
	BEGIN
		DECLARE @D INT
		IF @A<0 SET @A=0
		IF @B<0 SET @B=0
		IF @C<0 SET @C=0
		SET @D=@A+@B+@C
		RETURN @D
	END

