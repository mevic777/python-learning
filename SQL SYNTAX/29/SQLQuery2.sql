	IIF(MONTH(GETDATE())>=MONTH(DATA_NASTERII),YEAR(GETDATE())-YEAR(DATA_NASTERII),YEAR(GETDATE())-YEAR(DATA_NASTERII)-1) AS ANI,
	IIF(MONTH(GETDATE())>=MONTH(DATA_NASTERII),MONTH(GETDATE())-MONTH(DATA_NASTERII),11-MONTH(DATA_NASTERII)+MONTH(GETDATE))) AS LUNI,
