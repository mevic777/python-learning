--SA SE CALCULEZE PENTRU FIECARE DEPARTAMENT NUMARUL DE PERSOANE
SELECT *,
	(
		SELECT COUNT(DISTINCT(ANGAJAT_ID)) FROM ANGAJARE A
		WHERE A.DEPARTAMENT_ID=D.DEPARTAMENT_ID
	) AS NR
	FROM DEPARTAMENT D

CREATE FUNCTION NR_PERS_DEP(@ID_DEP INT)
	RETURNS INT
	BEGIN
		RETURN
		(
			SELECT COUNT(DISTINCT(ANGAJAT_ID)) FROM ANGAJARE A
			WHERE A.DEPARTAMENT_ID=@ID_DEP
		)
	END

SELECT *,DBO.NR_PERS_DEP(D.DEPARTAMENT_ID) AS NR
	FROM DEPARTAMENT D

