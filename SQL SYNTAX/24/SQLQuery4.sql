--SA SE GASEASCA DIMENSIUNEA MEDIE A ECRANILOR
--LAPTOPURILOR PRODUSE DE FIECARE PRODUCATOR IN PARTE
--SA SE AFISEZE 

SELECT PRODUCATOR,
(
	SELECT AVG(ECRAN) FROM LAPTOPURI L INNER JOIN PRODUSE P ON P.MODEL_ID=L.MODEL_ID
	WHERE P.PRODUCATOR=PP.PRODUCATOR
) DIMENSIUNE
FROM PRODUSE PP
GROUP BY PRODUCATOR

--SA SE GASEASCA PRODUCATORII CARE PRODUC CEL PUTIN 3 PC-URI
--AFLATI PRODUCATOR SI NUMAR DE MODELE

SELECT PRODUCATOR,
(
	SELECT COUNT(DISTINCT(PC.MODEL_ID)) FROM PRODUSE P INNER JOIN PC ON P.MODEL_ID=PC.MODEL_ID
	WHERE PP.PRODUCATOR=P.PRODUCATOR
) NUMERE
FROM PRODUSE PP
GROUP BY PRODUCATOR
HAVING 
(
	SELECT COUNT(DISTINCT(PC.MODEL_ID)) FROM PRODUSE P INNER JOIN PC ON P.MODEL_ID=PC.MODEL_ID
	WHERE PP.PRODUCATOR=P.PRODUCATOR
)>1

--SA SE GASEASCA PRETUL MAXIMAL AL PC-URILOR PRODUSE DE FIECARE PRODUCATOR IN PARTE
SELECT PRODUCATOR,
(
	SELECT MAX(PRET) FROM PC INNER JOIN PRODUSE P ON P.MODEL_ID=PC.MODEL_ID
	WHERE P.PRODUCATOR=PP.PRODUCATOR
) PRET_MAX
FROM PRODUSE PP
GROUP BY PRODUCATOR
HAVING
(
	SELECT MAX(PRET) FROM PC INNER JOIN PRODUSE P ON P.MODEL_ID=PC.MODEL_ID
	WHERE P.PRODUCATOR=PP.PRODUCATOR
) IS NOT NULL

