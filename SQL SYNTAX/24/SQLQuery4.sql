--SA SE GASEASCA PRODUCATORII DE IMPRIMANTE CARE PRODUC SI PC-URI CU CEL MAI MIC
--VOLUM RAM

SELECT PRODUCATOR
	FROM PRODUSE P INNER JOIN IMPRIMANTE I ON P.MODEL_ID=I.MODEL_ID
INTERSECT
SELECT PRODUCATOR
	FROM PRODUSE P INNER JOIN PC ON P.MODEL_ID=PC.MODEL_ID
	WHERE RAM=
	(
		SELECT MIN(RAM) FROM PC
	)

SELECT PRODUCATOR
	FROM IMPRIMANTE I INNER JOIN PRODUSE P ON P.MODEL_ID=I.MODEL_ID
	WHERE PRODUCATOR IN
	(
		SELECT PRODUCATOR FROM PC INNER JOIN PRODUSE P ON P.MODEL_ID=PC.MODEL_ID
		WHERE RAM=
		(
			SELECT MIN(RAM) FROM PC
		)
	)
	GROUP BY PRODUCATOR

