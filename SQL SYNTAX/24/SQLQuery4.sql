--IIF - IF PRESCURTAT
SELECT NUME,PRENUME,DATA_NASTERII,
	IIF(GEN=1,'M','F') AS GEN,
	(
		CASE
		WHEN GEN=1 THEN 'M'
		ELSE 'F'
		END
	) AS GEN2,
	(
		CASE
		WHEN GEN=1 THEN 'M'
		WHEN GEN=0 THEN 'F'
		END
	) AS GEN3
	FROM ANGAJAT

