SELECT NUME,PRENUME,DATA_NASTERII,DAY(DATA_NASTERII) AS ZI,
(
	CASE
	MONTH(DATA_NASTERII)
	WHEN 1 THEN 'IANUARIE'
	WHEN 2 THEN 'FEBRUARIE'
	WHEN 3 THEN 'MARTIE'
	WHEN 4 THEN 'APRILIE'
	WHEN 5 THEN 'MAI'
	WHEN 6 THEN 'IUNIE'
	WHEN 7 THEN 'IULIE'
	WHEN 8 THEN 'AUGUST'
	WHEN 9 THEN 'SEPTEMBRIE'
	WHEN 10 THEN 'OCTOMBRIE'
	WHEN 11 THEN 'NOIEMBRIE'
	WHEN 12 THEN 'DECEMBRIE'
	ELSE '-'
	END
) AS LUNA,
	YEAR(DATA_NASTERII) AS AN
	FROM ANGAJAT