ALTER PROCEDURE P1 @PRET FLOAT=3000,@VITEZA DECIMAL(4,0)=1500
	AS
	SELECT *
		FROM PC WHERE PRET>@PRET AND VITEZA<@VITEZA;

EXEC P1 @VITEZA=6000

--SA SCRIE O PROCEDURA CARE VA AVEA DREPT EFECT CAUTAREA INFORMATIEI DUPA PRET A PRODUSELOR
CREATE FUNCTION TAB_VIR()
	RETURNS TABLE
	AS
	RETURN 
	(
		SELECT 'PC' AS TIP,MODEL_ID,VITEZA,PRET
			FROM PC
			UNION
		SELECT 'LAPTOP',MODEL_ID,VITEZA,PRET
			FROM LAPTOPURI
			UNION
		SELECT 'IMPRIMANTE',MODEL_ID,0,PRET
			FROM IMPRIMANTE
	)

CREATE PROCEDURE P2 @SUMA1 FLOAT=0,@SUMA2 FLOAT=0
	AS
	SELECT *
		FROM DBO.TAB_VIR() WHERE PRET BETWEEN @SUMA1 AND @SUMA2

EXEC P2 @SUMA2=6000

--SA SE SCRIE O PROCEDURA CARE VA CALCULA PRETUL MEDIU AL UNUI ANUMIT TIP DE PRODUS
ALTER PROCEDURE P3 @TIP VARCHAR(250)--,@REZ FLOAT OUTPUT
	AS
	DECLARE @REZ FLOAT;
	IF @TIP='PC'
	BEGIN
		SET @REZ=(SELECT AVG(PRET) FROM PC)	
	END;
	ELSE IF @TIP='LAPTOP'
	BEGIN
		SET @REZ=AVG(PRET) FROM LAPTOPURI
	END;
	ELSE IF @TIP='IMPRIMANTE'
	BEGIN
		SET @REZ=AVG(PRET) FROM IMPRIMANTE
	END;

EXEC P3 @TIP='PC'

