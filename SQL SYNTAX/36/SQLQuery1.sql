--SA SE CREEZE O PROCEDURA CARE VA RETURNA VALORILE TABELELOR UNIFICATE
CREATE PROCEDURE P5
	AS
	SELECT 'PC' AS TIP,MODEL_ID,VITEZA,PRET FROM PC
		UNION
	SELECT 'LAPTOP',MODEL_ID,VITEZA,PRET FROM LAPTOPURI
		UNION
	SELECT 'IMPRIMANTE',MODEL_ID,0,PRET FROM IMPRIMANTE

--EFECTUAM INSCRIEREA PROCEDURII CREATE IN TABELA VIRTUALA
CREATE PROCEDURE P6 @TIP VARCHAR(10)
	AS
	CREATE TABLE #MYTABLE(TIP VARCHAR(20),MODEL_ID INT,VITEZA FLOAT,PRET FLOAT)
		INSERT INTO #MYTABLE EXEC P5
		IF @TIP='MAXIM' BEGIN
		SELECT * FROM #MYTABLE
			WHERE PRET=(SELECT MAX(PRET) FROM #MYTABLE)
			END;
		ELSE BEGIN
		SELECT * FROM #MYTABLE
			WHERE PRET=(SELECT MIN(PRET) FROM #MYTABLE)
			END;
EXEC P6 @TIP='MAXIM'