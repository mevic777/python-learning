CREATE TRIGGER VERIF_PRET ON PC
	AFTER UPDATE
	AS
	DECLARE @PRET_NOU FLOAT;
	DECLARE @PRET_VECHI FLOAT;
	IF UPDATE(PRET)
		BEGIN
			SET @PRET_NOU=
			(
				SELECT PRET FROM INSERTED
			);
			SET @PRET_VECHI=
			(
				SELECT PRET FROM DELETED
			);
			PRINT 'S-A MODIFICAT PRETUL DIN '+CAST(@PRET_VECHI AS VARCHAR(20))+' LEI IN '+CAST(@PRET_NOU AS VARCHAR(20))+' LEI';
		END;

