CREATE FUNCTION F1(@X INT)
	RETURNS INT
	BEGIN
		DECLARE @Y INT;
		SET @Y=0;
		WHILE @X<=5
			BEGIN
				SET @Y=@Y+@X;
				SET @X=@X+1;
			END
		RETURN @Y;
	END;

